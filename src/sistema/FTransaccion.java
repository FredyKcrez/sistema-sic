    private void cargarCuentas()
    {
        conexion = ConexionBD.Conectar();
        try{
        st = conexion.createStatement();
        res = st.executeQuery("SELECT id, nombre FROM CUENTAS EXCEPT SELECT c2.id, c2.nombre "
                + "FROM cuentas AS c1 INNER JOIN cuentas AS c2 ON c1.idpadre=c2.id");
        while(res.next())
        {
            String cuenta = res.getString("id") + " " + res.getString("nombre");
            System.out.println(cuenta);
        }
        }catch(Exception e){JOptionPane.showMessageDialog(null, "ERROR \n"+e);}
    }
    
